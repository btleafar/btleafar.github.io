#!/bin/bash

# Create the main directory
mkdir my-website

# Navigate into the main directory
cd my-website

# Create the index.html file
touch index.html

# Create the 'css' and 'js' directories
mkdir css js

# Create the styles.css and scripts.js files
touch css/styles.css js/scripts.js

# Print a message to indicate the script has finished running
echo "Directory structure set up successfully!"